#include "minishell.h"

void	echo(char **input)
{
	int i;
	int j;

	i = 1;
	while (input[i] != '\0')
	{
		j = 0;
		ft_putstr("i");
		while (input[i][j] != '\0')
		{
			if (input[i][j] != '"')
				ft_putchar(input[i][j]);
			j++;
			ft_putstr("j");
		}
		i++;
	}
}
