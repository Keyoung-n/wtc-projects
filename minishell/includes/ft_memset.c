#include "libft.h"

void	*ft_memset(void *dst, int c, size_t n)
{
	size_t			cmp;
	unsigned char	*ptrmem;

	cmp = 0;
	ptrmem = (unsigned char *)dst;
	while (cmp < n)
	{
		ptrmem[cmp] = (unsigned char)c;
		cmp++;
	}
	return (dst);
}
