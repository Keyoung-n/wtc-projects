#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int			v[3];
	const char	*s[2];

	s[0] = s1;
	s[1] = s2;
	v[0] = -1;
	while (++v[0] < (int)n)
	{
		v[1] = s[0][v[0]];
		v[2] = (int)s[1][v[0]];
		if (s[0][v[0]] < 0)
			v[1] += 256;
		if (s[1][v[0]] < 0)
			v[2] += 256;
		if (s[0][v[0]] != s[1][v[0]])
			return (v[1] - v[2]);
	}
	return (0);
}
