#include "libft.h"

int		ft_atoi(const char *str)
{
	int		v[3];

	v[0] = 0;
	v[2] = 1;
	v[1] = -1;
	if ((str[0] == '-' || str[0] == '+') && (v[1] += 1) >= 0)
		v[2] += -2 * (str[0] == '-');
	while (str[v[1] + 1] < 43)
		v[1]++;
	while (str[++v[1]] && str[v[1]] >= '0' && str[v[1]] <= '9')
		v[0] = v[0] * 10 + (str[v[1]] - 48);
	return (v[0] * v[2]);
}
