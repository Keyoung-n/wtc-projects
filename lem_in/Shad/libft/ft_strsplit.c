#include "libft.h"

char    **ft_strsplit(char const *s, char c)
{
	int     v[4];
	char    **n;

	v[0] = -1;
	v[1] = -1;
	while (s[++v[1]])
		v[0] += (s[v[1]] != c && (s[v[1] - 1] == c || v[1] == 0));
	n = (char **)malloc(sizeof(char *) * (v[0] + 1) + 1);
	v[0] = -1;
	v[1] = -1;
	v[2] = -1;
	while (s[++v[2]] && (v[3] = -1))
	{
		if ((s[v[2]] != c && (s[v[2] - 1] == c || v[2] == 0))\
			&& (v[1] = -1))
		{
			while (s[++v[3] + v[2]] && s[v[3] + v[2]] != c)
				;
			n[++v[0]] = (char *)malloc(sizeof(char) * (v[3] + 1));
		}
		if (s[v[2]] != c && (n[v[0]][++v[1] + 1] = '\0') >= 0)
			n[v[0]][v[1]] = s[v[2]];
	}
	return (n);
}
