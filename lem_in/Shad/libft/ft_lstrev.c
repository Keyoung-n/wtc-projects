#include "libft.h"

t_list	*ft_lstrev(t_list *lst)
{
	t_list	*a[3];

	a[1] = lst;
	a[0] = 0;
	while (a[1])
	{
		a[2] = a[1]->next;
		a[1]->next = a[0];
		a[0] = a[1];
		a[1] = a[2];
	}
	lst = a[0];
	return (lst);
}
