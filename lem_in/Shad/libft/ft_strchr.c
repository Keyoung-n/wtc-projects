#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	v[2];

	v[1] = -1;
	while (c != '\0' && s[++v[1]])
		if (s[v[1]] == c)
			return ((char *)&s[v[1]]);
	return (0);
}
