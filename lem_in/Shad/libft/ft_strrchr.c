#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		v[2];

	v[0] = -1;
	v[1] = -1;
	while (c != '\0' && s[++v[1]])
		if (s[v[1]] == c)
			v[0] = v[1];
	return ((char *)&s[v[0]]);
}
