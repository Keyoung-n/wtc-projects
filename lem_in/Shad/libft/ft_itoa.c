#include "libft.h"

char	*ft_itoa(int n)
{
	int		t[13];
	char	*s;

	t[11] = -1;
	s = (char *)ft_memalloc(sizeof(char) * 11);
	t[12] = -1;
	if (n == 0)
		t[++t[12]] = 0;
	if (s && n < 0 && (s[++t[11]] = '-'))
		n = -n;
	while (n > 0 && ((t[++t[12]] = n % 10) >= 0))
		n /= 10;
	while (s && --t[12] + 1 > -1 && (s[++t[11]] = 48 + t[t[12] + 1]))
		s[t[11] + 1] = '\0';
	return (s);
}
