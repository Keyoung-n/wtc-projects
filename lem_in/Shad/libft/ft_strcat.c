#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int		v;

	v = (int)strlen(s1);
	while (*(++s2 - 1) && (s1[++v - 1] = *(s2 - 1)))
		s1[v] = '\0';
	return (s1);
}
