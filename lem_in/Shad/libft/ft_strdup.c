#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s;
	int		x;

	x = -1;
	s = (char *)ft_memalloc(sizeof(char) * sizeof(&s1));
	while (*(++s1 - 1))
		s[++x] = *(s1 - 1);
	return (s);
}
