#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int		v[3];

	v[0] = -1;
	while (s1[++v[0]] || s2[v[0]])
	{
		v[1] = s1[v[0]];
		v[2] = s2[v[0]];
		if (s1[v[0]] < 0)
			v[1] += 256;
		if (s2[v[0]] < 0)
			v[2] += 256;
		if (v[1] != v[2])
			return (v[1] - v[2]);
	}
	return (0);
}
