#include "libft.h"

int		ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int		x[2];

	x[1] = 0;
	x[0] = -1;
	while (s1[++x[0]] && s2[x[0]] && (x[0] < ((int)n)) && x[1] == 0)
		if (s1[x[0]] != s2[x[0]])
			x[1]++;
	if (x[1] == 0)
		return (1);
	return (0);
}
