#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char	*s1;
	char	*s2;
	size_t	i;

	s1 = (char*)dst;
	s2 = (char*)src;
	i = 0;
	while (i < len)
	{
		s1[i] = s2[i];
		i += 1;
	}
	return (s1);
}
