#include <string.h>

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t	i;

	i = 0;
	while ((dst[i] != '\0' && src[i] != '\0') && i < len)
	{
		dst[i] = src[i];
		i++;
	}
	while (i < len && dst[i] != '\0')
	{
		dst[i] = '\0';
		i++;
	}
	return (dst);
}
